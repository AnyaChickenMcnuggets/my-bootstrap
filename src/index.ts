function sum(a: number, b: number){
  return a+b;
}

function diff(a: number, b: number){
  return a-b;
}

function mul(a: number, b: number){
  return a*b;
}

function div(a: number, b: number){
  return a/b;
}

let x: number = 10;
let y: number = 5;

console.log(sum(x,y));
console.log(diff(x,y));
console.log(mul(x,y));
console.log(div(x,y));